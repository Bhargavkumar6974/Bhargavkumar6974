package operators;

public class IncrementAndDecrement 
{
	   

		public static void main(String[] args) 
	    {
		
		int a=100,b=20;
		
		System.out.println("prefix inc "+(++a));
		System.out.println("postfix inc "+(a++));
		System.out.println("postfix inc "+(a++));
		
		System.out.println("prefix dec "+(--b));
		System.out.println("postfix dec "+(b--));
		System.out.println("postfix dec "+(b--));
		
	    }
		}
	    