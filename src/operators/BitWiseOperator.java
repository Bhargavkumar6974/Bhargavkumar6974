package operators;

public class BitWiseOperator {

							public static void main(String[] args) 
						    {
							
							int a=10,b=9;
							
							System.out.println("BW and  " +(a&b));
							System.out.println("BW or  " +(a|b));
							System.out.println("BW exor  " +(a^b));
							System.out.println("BW not  " +(~a));
							System.out.println("BW not  " +(~b));
					
						    }
							}
						    
